import math


# math has useful constants.
print(math.pi)
print(math.e)

print()

# sqrt() finds a square root.
print(math.sqrt(2))
print(math.sqrt(3))

print()

# Trigonometric functions use radians.
print(math.cos(math.pi), 10)
print(math.sin(math.pi), 10)
print(math.atan(math.sqrt(3)))
print(math.degrees(math.atan(math.sqrt(3))), 2)

print()

# degrees() and radians() convert angle units.
print(math.degrees(math.pi))
print(math.radians(60), 4)
