# Save two names in simple variables.
# When you put text inside single/double quotation marks, the text inside is considered a string
name1 = "Pikachu"
name2 = "Charmander"

# Print a basic string. 
print("Hello, world!")

# Insert values with format().
print("Hello, {}. My name is {}.".format(name1, name2))

# f-strings are another common way to format text.
print(f"Hello, {name1}. My name is {name2}.")
