# Save two strings.
a = "abcdef"
b = "1"

# Print strings and their types.
print(a, type(a))
print(b, type(b))

print()

# Read characters with indexes.
print(a[0])
print(a[1])
print(a[-1])

# Slicing gets part of a string.
print(a[0:2])
print(a[0:-1])

print()

# len() is useful for counting characters.
print(len(a))

# ord() changes a character into a number.
print(ord("a"))
print(ord("b"))
