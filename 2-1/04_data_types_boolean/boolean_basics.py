# Save two boolean values.
a = True
b = False

# and, or, not are basic boolean operators.
print(a, type(a))
print(b, type(b))
print(a and b, type(a and b))
print(a or b, type(a or b))
print(not a, type(not a))

print()

# Comparison also gives boolean results.
x = 2
y = 10

print(x > y, type(x > y))
print(x < y, type(x < y))
print(x == y, type(x == y))
